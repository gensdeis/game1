	struct RpcPacket
	{
		int32_t len_;
		mpe_rpc_cmd command_;
#ifdef _USE_NEW_PACKET_HEADER
		int32_t param_;
#endif
		mpe_error error_code_;
		uint8_t tag_;

		RpcPacket() : len_(0), command_(mpe_rpc_cmd::none), error_code_(mpe_error::error_none), tag_(0)
		{
		}
		RpcPacket(mpe_rpc_cmd cmd) : len_(0), command_(cmd), error_code_(mpe_error::error_none), tag_(0)
		{
		}
		RpcPacket(mpe_rpc_cmd cmd, uint8_t tag) : len_(0), command_(cmd), error_code_(mpe_error::error_none), tag_(tag)
		{
		}
		virtual ~RpcPacket()
		{
		}
		template<typename T_BUFFER> void Serialize(T_BUFFER* pBuffer)
		{
			if (T_BUFFER::cIsWriter)
			{
				pBuffer->Serialize4((int32_t*)&len_);
				pBuffer->Serialize2((uint16_t*)&command_);
#ifdef _USE_NEW_PACKET_HEADER
				pBuffer->Serialize4((int32_t*)&param_);
#endif
			}
			pBuffer->Serialize4(&error_code_);
			pBuffer->Serialize1((uint8_t*)&tag_);

			pBuffer->SetSerializeCommand(static_cast<uint16_t>(command_));
		}
		template<typename T_BUFFER> void SerializeEnd(T_BUFFER* pBuffer, int16_t length)
		{
			if (T_BUFFER::cIsWriter)
			{
				pBuffer->SerializeEnd(length);
			}
		}
	};
